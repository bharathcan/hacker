import java.util.Scanner;
class Switch1{
public static void main(String[] args)
{
int age;
Scanner sc=new Scanner(System.in);
System.out.println("Enter your age\n");
age =sc.nextInt();
switch(age){
case 1:
System.out.println("bharath"+age);
break;
case 2:
System.out.println("bharu"+age);
break;
case 3:
System.out.println("bharathi"+age);
break;
}
}
}
